package day02;

public class Day02Ex {

	public static void main(String[] args) {
		//안녕하세요
		System.out.print("hello world!");
		System.out.print("문자열 값"); //문자열 값
		System.out.print("A");      //문자형 값
		System.out.print(100);      //정수형 값
		System.out.print(3.14);     //실수형 값
		
		
		System.out.println("문자열 값"); //문자열 값
		System.out.println("A");      //문자형 값
		System.out.println(100);      //정수형 값
		System.out.println(3.14);     //실수형 값
		//sysout 커서 뒤에 Ctrl + Space 누르면 해당 메서드 자동완성
		
		
		//format : 형식 지정
		System.out.printf("%s","문자열 값"); //문자열 값
		System.out.printf("%c",'A');      //문자형 값
		System.out.printf("%d",100);      //정수형 값
		System.out.printf("%f",3.14);     //실수형 값
	}

}
